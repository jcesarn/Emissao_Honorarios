    private void preencherTableCrudCliente() {
        crud_cliente_id.setText("");
        crud_cliente_nome.setText("");
        crud_cliente_endereco.setText("");
        crud_cliente_latitude.setText("");
        crud_cliente_longitude.setText("");

        List<Cliente> lista_clientes = new dao.DAOcliente().buscarTodosClientes(classes.OperadorAutenticado.operadorAutenticado.getEmpresa().getId());
        tabela_crud_clientes.getColumnModel().getColumn(0).setPreferredWidth(0);
        tabela_crud_clientes.getColumnModel().getColumn(1).setPreferredWidth(160);
        tabela_crud_clientes.getColumnModel().getColumn(2).setPreferredWidth(160);
        DefaultTableModel tabela_crud_clientes = (DefaultTableModel) this.tabela_crud_clientes.getModel();
        tabela_crud_clientes.setNumRows(0);
        for (Cliente cl : lista_clientes) {
            Object[] dados = {cl.getId(), cl.getNome(), cl.getEndereco()};
            tabela_crud_clientes.addRow(dados);
        }
    }

    private void atualizarPainelMapaCrudCliente(String longi, String latid) {
        //https://open.mapquestapi.com/
        if (!longi.isEmpty() && !latid.isEmpty()) {
            mapa_cliente.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/mapa_car.jpg")));
            new Thread() {
                @Override
                public void run() {
                    try {
                        String imageUrl = "https://open.mapquestapi.com/staticmap/v4/getmap?key=8uaQlNpaSULJYWbdm30lD6TzLToUN9Ge&size=413,184&zoom=17&center=" + longi + "," + latid + "";
                        String destinationFile = "mapa.jpg";
                        URL url = new URL(imageUrl);
                        OutputStream os;
                        try (InputStream is = url.openStream()) {
                            os = new FileOutputStream(destinationFile);
                            byte[] b = new byte[2048];
                            int length;
                            while ((length = is.read(b)) != -1) {
                                os.write(b, 0, length);
                            }
                            mapa_cliente.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/mapa.jpg")));
                            ImageIcon imageIcon = new ImageIcon(url);
                            mapa_cliente.setIcon(imageIcon);
                            mapa_cliente.repaint();
                            os.close();
                        } catch (IOException | NullPointerException e) {
                            mapa_cliente.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/mapa_ind.jpg")));
                        }
                    } catch (IOException e) {
                        mapa_cliente.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/mapa_ind.jpg")));
                        e.printStackTrace();
                        System.exit(1);
                    }
                }
            }.start();
        } else {
            mapa_cliente.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/mapa_ind.jpg")));
        }
        painel_mapa_crudcliente.repaint();
        painel_mapa_crudcliente.validate();
        repaint();
        validate();
    }

