    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (crud_cliente_id.getText() == null || crud_cliente_id.getText().isEmpty()) {
            String nome = crud_cliente_nome.getText();
            String endereco = crud_cliente_endereco.getText();
            String lon = crud_cliente_longitude.getText();
            String lat = crud_cliente_latitude.getText();
            int id_emp = classes.OperadorAutenticado.operadorAutenticado.getEmpresa().getId();

            Cliente clt = new Cliente(id_emp, nome, endereco);
            clt.setLatitude(lat);
            clt.setLongitude(lon);

            crud_cliente_nome.setBackground(new Color(255, 255, 255));
            crud_cliente_endereco.setBackground(new Color(255, 255, 255));
            crud_cliente_longitude.setBackground(new Color(255, 255, 255));
            crud_cliente_latitude.setBackground(new Color(255, 255, 255));

            if (crud_cliente_nome.getText().isEmpty() || crud_cliente_nome.getText().startsWith(" ") || crud_cliente_nome.equals("") || crud_cliente_nome.getText() == null) {
                javax.swing.JOptionPane.showMessageDialog(null, "Preencha o campo corretamente", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
                crud_cliente_nome.setBackground(new Color(255, 196, 196));
                crud_cliente_nome.requestFocus(true);
                crud_cliente_nome.setText("");
                return;
            }
            if (crud_cliente_endereco.getText().isEmpty() || crud_cliente_endereco.getText().startsWith(" ") || crud_cliente_endereco.equals("") || crud_cliente_endereco.getText() == null) {
                javax.swing.JOptionPane.showMessageDialog(null, "Preencha o campo corretamente", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
                crud_cliente_endereco.setBackground(new Color(255, 196, 196));
                crud_cliente_endereco.requestFocus(true);
                crud_cliente_endereco.setText("");
                return;
            }
            if (!crud_cliente_latitude.getText().isEmpty() && crud_cliente_longitude.getText().isEmpty() || crud_cliente_longitude.getText().startsWith(" ") || crud_cliente_longitude.equals("") || crud_cliente_longitude.getText() == null) {
                javax.swing.JOptionPane.showMessageDialog(null, "Preencha o campo corretamente", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
                crud_cliente_longitude.setBackground(new Color(255, 196, 196));
                crud_cliente_longitude.requestFocus(true);
                crud_cliente_longitude.setText("");
                return;
            }
            if (!crud_cliente_longitude.getText().isEmpty() && crud_cliente_latitude.getText().isEmpty() || crud_cliente_latitude.getText().startsWith(" ") || crud_cliente_latitude.equals("") || crud_cliente_latitude.getText() == null) {
                javax.swing.JOptionPane.showMessageDialog(null, "Preencha o campo corretamente", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
                crud_cliente_latitude.setBackground(new Color(255, 196, 196));
                crud_cliente_latitude.requestFocus(true);
                crud_cliente_latitude.setText("");
                return;
            }

            if (!new dao.DAOcliente().verNomeClientes(nome) && !new dao.DAOcliente().verEnderClientes(endereco)) {
                if (new dao.DAOcliente().cadastrarCliente(clt)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Cliente cadastrado", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    preencherTableCrudCliente();
                } else {
                    javax.swing.JOptionPane.showMessageDialog(null, "Falha ao cadastrar cliente", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Já existe um cliente com esse nome/endereço", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
            }
        } else {
            crud_cliente_id.setText("");
            crud_cliente_nome.setText("");
            crud_cliente_endereco.setText("");
            crud_cliente_latitude.setText("");
            crud_cliente_longitude.setText("");
            crud_cliente_nome.requestFocus(true);
            javax.swing.JOptionPane.showMessageDialog(null, "Campos limpos para novo cadastro", "Atenção", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

