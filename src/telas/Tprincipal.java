    private void btn_mov_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mov_atualizarActionPerformed
        if (!txt_mov_valorhono.getText().isEmpty() && !txt_mov_valorinss.getText().isEmpty() && !txt_mov_valorcomp.getText().isEmpty() && !txt_mov_valorsalario.getText().isEmpty()
                && !txt_mov_deschono.getText().isEmpty() && !txt_mov_descinss.getText().isEmpty() && !txt_mov_desccomp.getText().isEmpty() && !txt_mov_descsalario.getText().isEmpty()) {
            this.setClienteComboCrudHonorario();
            DAOhonorario daohono = new DAOhonorario();
            this.h = daohono.buscar(this.c.getId(), 2, null, null);
            String desc1 = txt_mov_deschono.getText();
            String desc2 = txt_mov_descinss.getText();
            String desc3 = txt_mov_desccomp.getText();
            String desc4 = txt_mov_descsalario.getText();
            Double hono = Double.parseDouble(txt_mov_valorhono.getText());
            Double inss = Double.parseDouble(txt_mov_valorinss.getText());
            Double comp = Double.parseDouble(txt_mov_valorcomp.getText());
            Double salario = Double.parseDouble(txt_mov_valorsalario.getText());
            this.h.setDesc_1(desc1);
            this.h.setDesc_2(desc2);
            this.h.setDesc_3(desc3);
            this.h.setDesc_4(desc4);
            this.h.setVal_1(hono);
            this.h.setVal_2(inss);
            this.h.setVal_3(comp);
            this.h.setVal_4(salario);
            daohono.atualizar(this.h);
            btn_mov_atualizar.setEnabled(false);
            btn_mov_remover.setEnabled(false);
            resetarCamposCrudHonorario();
            javax.swing.JOptionPane.showMessageDialog(null, "Honorário atualizado", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Campos inválidos", "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_mov_atualizarActionPerformed

