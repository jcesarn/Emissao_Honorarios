    private void menuClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuClicked
        // CADASTROS        
        if (evt.getSource() == sm_cliente) {
            mostrarPainel(painel_crudCliente);
            preencherTableCrudCliente();
            atualizarPainelMapaCrudCliente("-19.7186725", "-47.9577441");
        }
        if (evt.getSource() == sm_operador) {
            mostrarPainel(painel_crudOperador);
            preencherCamposCrudOperador();
        }
        if (evt.getSource() == sm_empresa) {
            mostrarPainel(painel_crudEmpresa);
            preencherCamposCrudEmpresa();
        }

        //MOVIMENTOS        
        if (evt.getSource() == sm_sm_automatico) {
            mostrarPainel(painel_movAutomatico);
        }
        if (evt.getSource() == sm_sm_manual) {
            mostrarPainel(painel_movManual);
            preencherNomesClientesCrudHonorario();
            btn_mov_atualizar.setEnabled(false);
            btn_mov_remover.setEnabled(false);
        }

        // RELATORIOS
        if (evt.getSource() == sm_relatorioClientes) {
            mostrarPainel(painel_relatorioClientes);
            popularComboMes(combo_mes);
            popularComboAno(combo_ano);
        }
        if (evt.getSource() == sm_reciboHonorarios) {
            mostrarPainel(painel_reciboHonorarios);
            popularListaClientesRebibosHono();
            popularComboMes(combo_mesRecibo);
            popularComboAno(combo_anoRecibo);
            popularComboMes(combo_mesReciboInd);
            popularComboAno(combo_anoReciboInd);
        }
    }//GEN-LAST:event_menuClicked

